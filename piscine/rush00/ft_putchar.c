#include <unistd.h>

void	ft_putchar(int i, int j, int x, int y)
{
	char	c;

	while (i < x)
	{
		
		if ((j == 0) && (i == 0 || i == x - 1))
			c = 'A';
		else if (j == y - 1 && (i == 0 || i == x - 1))
			c = 'C';
		else if ((j == 0 || j == y - 1) || (i == 0 || i == x - 1))
			c = 'B';
		else if ((j != 0 && j != y - 1) && (i != 0 && i != x - 1))
			c = ' ';
		write (1, &c, 1);
		i++;
	}
}
